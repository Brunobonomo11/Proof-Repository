# Quadratic Formula

a = int(input("Ingresar a :"))
b = int(input("Ingresar b :"))
c = int(input("Ingresar c :"))

root1 = (-b + (b*b - 4*a*c)**0.5) / (2*a)
root2 = (-b + (b*b - 4*a*c)**0.5) / (2*a)

print(root1)
print(root2)


